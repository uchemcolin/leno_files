     * Register any application services.
     *
     * @return void
     */
    public function register()
    {
        //
    }

    /**
     * Bootstrap any application services.
     *
     * @return void
     */
    public function boot()
    {
        //
        //You can unguard all your Models instead of setting the protected 
        //guarded variable to an empty array in each App\Models\YourModel
        Model::unguard();
    }
}
